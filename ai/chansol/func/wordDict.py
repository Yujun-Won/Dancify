def wordDict(text):
    text = text.replace(' ', '')
    WD = {0:('시작', '시작하다', '시작해', '시작해라', '시작할게', '시작할게요', '시작합니다', '시작해봅시다'),
          1:('이전', '이전으로', '이정', '이전구간으로'),
          2:('다음', '다음으로', '다음순서로', '다음구간으로', '당', '담'),
          3:('한번더', '함더', '한번더해', '함덕', '한번더할게'),
          4:('종료', '종료하다', '종료해', '종료해라', '종료할게', '종료할게요', '종료합니다', '종료해봅시다'),}
    for i in range(len(WD)):
        if text in WD[i]:
            return i
    return 5

